const CATEGORIES = [
  { label: 'Programming', keys: ['Python', 'C / C++', 'JavaScript', 'SQL'] },
  { label: 'Frontend', keys: ['HTML5', 'CSS3', 'React'] },
  { label: 'Backend', keys: ['Django', 'REST APIs'] },
  { label: 'Tools', keys: ['GitHub', 'VS Code', 'Figma', 'Postman', 'Vercel'] },
]

export default function Skills() {
  const categoryRefs = useRef([])

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add('cat-visible')
          } else {
            entry.target.classList.remove('cat-visible')
          }
        })
      },
      { threshold: 0.15 }
    )
    categoryRefs.current.forEach((el) => { if (el) observer.observe(el) })
    return () => observer.disconnect()
  }, [])

  return (
    <section id="skills" className="skills">
      <div>
        <h2 className="skills-title"><em>Technical</em> Skills</h2>
      </div>
      <div className="skills-categories">
        {CATEGORIES.map((cat, ci) => (
          <div
            key={cat.label}
            className="skills-category"
            ref={(el) => (categoryRefs.current[ci] = el)}
            style={{ '--cat-delay': ci * 0.1 + 's' }}
          >
            <h3 className="category-label">{cat.label}</h3>
            <div className="category-pills">
              {SKILLS.filter(s => cat.keys.includes(s.name)).map((skill, si) => (
                <div
                  key={skill.name}
                  className="skill-pill"
                  style={{
                    '--pill-color': skill.color,
                    '--pill-bg': skill.bg,
                    '--pill-delay': (ci * 0.1 + si * 0.05) + 's'
                  }}
                >
                  <span className="skill-icon">{skill.icon}</span>
                  <span className="skill-name">{skill.name}</span>
                </div>
              ))}
            </div>
          </div>
        ))}
      </div>
    </section>
  )
}